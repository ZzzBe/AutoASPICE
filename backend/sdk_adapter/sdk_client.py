    async def execute_step(
        self,
        agent_name: str,
        step_config: Dict[str, Any],
        step_index: int,
        node_id: str,
        output_stream: OutputStreamManager,
        context_files: List[str] = None,
        user_instruction: str = "",
        agent_domain: str = "",
        llm_config: Any = None,
        workflow_steps: List[Dict[str, Any]] = None,
    ) -> Dict[str, Any]:
        """
        Execute a single workflow step.

        Uses real LLM when llm_config has a valid api_key, otherwise falls
        back to V1 simulation templates.

        Args:
            agent_name: Name of the agent being executed
            step_config: Step definition dict (name, description, type, etc.)
            step_index: Zero-based step index
            node_id: Node identifier for output streaming
            output_stream: OutputStreamManager for streaming output
            context_files: List of context file paths
            user_instruction: Additional user instruction
            agent_domain: Agent's domain (adas, functional safety, etc.)
            llm_config: LLMConfigRequest with provider, api_key, model, base_url
            workflow_steps: All workflow steps for context

        Returns:
            Dict with step results (summary, output, deliverable_preview, etc.)
        """
        context_files = context_files or []
        workflow_steps = workflow_steps or []
        step_name = step_config.get("name", f"Step {step_index + 1}")
        step_desc = step_config.get("description", step_config.get("task", ""))

        # Try real LLM execution
        if self._has_llm_config(llm_config):
            try:
                return await self._execute_with_llm(
                    agent_name=agent_name,
                    step_name=step_name,
                    step_desc=step_desc,
                    step_index=step_index,
                    node_id=node_id,
                    output_stream=output_stream,
                    context_files=context_files,
                    user_instruction=user_instruction,
                    agent_domain=agent_domain,
                    llm_config=llm_config,
                    workflow_steps=workflow_steps,
                )
            except Exception as e:
                await output_stream.push(node_id, "output", {
                    "text": f"[WARN] LLM call failed ({e}), falling back to simulation...",
                    "step_index": step_index,
                    "timestamp": datetime.utcnow().isoformat(),
                })

        # Fallback: V1 simulation
        return await self._execute_simulated(
            agent_name=agent_name,
            step_config=step_config,
            step_index=step_index,
            node_id=node_id,
            output_stream=output_stream,
            agent_domain=agent_domain,
        )

    async def _execute_with_llm(
        self,
        agent_name: str,
        step_name: str,
        step_desc: str,
        step_index: int,
        node_id: str,
        output_stream: OutputStreamManager,
        context_files: List[str],
        user_instruction: str,
        agent_domain: str,
        llm_config: Any,
        workflow_steps: List[Dict[str, Any]],
    ) -> Dict[str, Any]:
        """Execute a step via real LLM with streaming output."""

        from llm.factory import create_provider

        provider = create_provider(
            provider=llm_config.provider,
            api_key=llm_config.api_key,
            model=llm_config.model,
            base_url=llm_config.base_url or None,
            temperature=0.3,
            max_tokens=8192,
        )

        # Build system prompt
        system_prompt = self._build_system_prompt(
            agent_name, agent_domain, workflow_steps, step_name
        )

        # Build user prompt
        user_prompt = self._build_user_prompt(
            step_name, step_desc, context_files, user_instruction, step_index, workflow_steps
        )

        messages = [
            ChatMessage(role="system", content=system_prompt),
            ChatMessage(role="user", content=user_prompt),
        ]

        # Stream LLM output
        full_text = []
        await output_stream.push(node_id, "output", {
            "text": f"--- {step_name} (via {llm_config.model}) ---",
            "step_index": step_index,
            "timestamp": datetime.utcnow().isoformat(),
        })

        try:
            async for chunk in provider.chat_stream(messages):
                if chunk.content:
                    full_text.append(chunk.content)
                    await output_stream.push(node_id, "output", {
                        "text": chunk.content,
                        "step_index": step_index,
                        "content_type": "llm_stream",
                        "timestamp": datetime.utcnow().isoformat(),
                    })
        finally:
            try:
                await provider.close()
            except Exception:
                pass

        full_output = "".join(full_text)
        summary = (
            f"Step '{step_name}' completed via {llm_config.model}. "
            f"Generated {len(full_output)} characters of analysis."
        )

        return {
            "step_index": step_index,
            "step_name": step_name,
            "summary": summary,
            "output": full_output,
            "output_lines": full_text,
            "deliverable_preview": full_output[:2000],
            "code_artifacts": self._extract_code_blocks(full_output),
            "model": llm_config.model,
            "provider": llm_config.provider,
            "timestamp": datetime.utcnow().isoformat(),
        }

    async def _execute_simulated(
        self,
        agent_name: str,
        step_config: Dict[str, Any],
        step_index: int,
        node_id: str,
        output_stream: OutputStreamManager,
        agent_domain: str = "",
    ) -> Dict[str, Any]:
        """V1 simulation fallback with domain-specific templates."""
        step_name = step_config.get("name", f"Step {step_index + 1}")
        domain_key = self._normalize_domain(agent_domain)
        outputs = self._domain_outputs.get(domain_key, self._generic_outputs)

        def _safe_fmt(text: str) -> str:
            return text.replace("{agent_name}", agent_name).replace("{domain}", agent_domain).replace("{step_name}", step_name)

        analysis_lines = [_safe_fmt(line) for line in outputs.get("analysis", self._generic_outputs["analysis"])]
        code_lines = [_safe_fmt(line) for line in outputs.get("code", self._generic_outputs["code"])]
        deliverable_lines = [_safe_fmt(line) for line in outputs.get("deliverable", self._generic_outputs["deliverable"])]

        full_output = []

        # Phase 1: Analysis
        for line in analysis_lines:
            await asyncio.sleep(random.uniform(0.1, 0.4))
            await output_stream.push(node_id, "output", {
                "text": line,
                "step_index": step_index,
                "timestamp": datetime.utcnow().isoformat(),
            })
            full_output.append(line)

        # Phase 2: Code
        if code_lines:
            await output_stream.push(node_id, "output", {
                "text": "--- Generating code artifacts ---",
                "step_index": step_index,
                "timestamp": datetime.utcnow().isoformat(),
            })
            for line in code_lines:
                await asyncio.sleep(random.uniform(0.05, 0.15))
                await output_stream.push(node_id, "output", {
                    "text": line,
                    "step_index": step_index,
                    "content_type": "code",
                    "timestamp": datetime.utcnow().isoformat(),
                })
                full_output.append(line)

        # Phase 3: Deliverable
        await output_stream.push(node_id, "output", {
            "text": f"\n--- {step_name} Deliverable Preview ---",
            "step_index": step_index,
            "timestamp": datetime.utcnow().isoformat(),
        })
        for line in deliverable_lines:
            await asyncio.sleep(random.uniform(0.05, 0.15))
            await output_stream.push(node_id, "output", {
                "text": line,
                "step_index": step_index,
                "content_type": "deliverable",
                "timestamp": datetime.utcnow().isoformat(),
            })
            full_output.append(line)

        await asyncio.sleep(random.uniform(0.2, 0.5))

        summary = (
            f"Step '{step_name}' completed successfully. "
            f"Generated {len(deliverable_lines)} lines of deliverable output "
            f"across {len(analysis_lines)} analysis items."
        )

        return {
            "step_index": step_index,
            "step_name": step_name,
            "summary": summary,
            "output": "\n".join(full_output),
            "output_lines": full_output,
            "deliverable_preview": "\n".join(deliverable_lines),
            "code_artifacts": "\n".join(code_lines) if code_lines else "",
            "timestamp": datetime.utcnow().isoformat(),
        }

    async def execute_full_workflow(
        self,
        agent_name: str,
        workflow_steps: List[Dict[str, Any]],
        node_id: str,
        output_stream: OutputStreamManager,
        context_files: List[str] = None,
        user_instruction: str = "",
        agent_domain: str = "",
        llm_config: Any = None,
    ) -> List[Dict[str, Any]]:
        """Execute an entire workflow sequentially."""
        results = []
        for i, step in enumerate(workflow_steps):
            result = await self.execute_step(
                agent_name=agent_name,
                step_config=step,
                step_index=i,
                node_id=node_id,
                output_stream=output_stream,
                context_files=context_files,
                user_instruction=user_instruction,
                agent_domain=agent_domain,
                llm_config=llm_config,
                workflow_steps=workflow_steps,
            )
            results.append(result)
        return results

