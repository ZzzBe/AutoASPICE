    def _build_system_prompt(
        self, agent_name: str, domain: str,
        workflow_steps: List[Dict[str, Any]], step_name: str,
    ) -> str:
        """Build system prompt for agent execution."""
        steps_summary = "\n".join(
            f"  {i+1}. {s.get('name', f'Step {i+1}')}"
            for i, s in enumerate(workflow_steps)
        ) if workflow_steps else ""

        return (
            f"You are {agent_name}, an automotive software engineering agent.\n"
            f"Domain: {domain}\n\n"
            f"## Workflow Context\n"
            f"Current step: {step_name}\n"
            f"Workflow steps:\n{steps_summary}\n\n"
            f"## Instructions\n"
            f"1. Provide detailed, technically accurate analysis specific to the automotive domain\n"
            f"2. Reference relevant standards (ISO 26262, ISO 21448, AUTOSAR, etc.) where applicable\n"
            f"3. Include code examples, tables, or structured data where helpful\n"
            f"4. Structure output with clear markdown headings (##, ###)\n"
            f"5. Be thorough but concise — prioritize actionable technical content\n"
            f"6. If you identify safety or compliance risks, flag them explicitly\n"
        )

    def _build_user_prompt(
        self, step_name: str, step_desc: str,
        context_files: List[str], user_instruction: str,
        step_index: int, workflow_steps: List[Dict[str, Any]],
    ) -> str:
        """Build user prompt for the step execution."""
        parts = [f"## Task: {step_name}\n"]

        if step_desc:
            parts.append(f"{step_desc}\n")

        # Add previous steps context
        if step_index > 0 and workflow_steps:
            parts.append("## Previous Steps Completed\n")
            for i in range(step_index):
                prev = workflow_steps[i]
                parts.append(f"- {prev.get('name', f'Step {i+1}')}")
            parts.append("")

        if context_files:
            parts.append("## Context Files\n")
            for f in context_files:
                parts.append(f"- {f}")
            parts.append("")

        if user_instruction:
            parts.append(f"## Additional Instructions\n{user_instruction}\n")

        parts.append(
            "\nPlease complete the analysis for this step. "
            "Structure your response with clear sections and include "
            "technical details, tables, and code where appropriate."
        )

        return "\n".join(parts)

    @staticmethod
    def _extract_code_blocks(text: str) -> str:
        """Extract code blocks from markdown text."""
        import re
        blocks = re.findall(r'```(?:\w+)?\n(.*?)```', text, re.DOTALL)
        return "\n\n".join(blocks) if blocks else ""

    def _normalize_domain(self, domain: str) -> str:
        """Normalize domain string to a known key."""
        domain_lower = domain.lower().replace("-", " ").replace("_", " ")
        if "adas" in domain_lower:
            return "adas"
        if "functional safety" in domain_lower or "safety" in domain_lower:
            return "functional safety"
        if "autosar" in domain_lower:
            return "autosar"
        if "sotif" in domain_lower:
            return "sotif"
        if "diagnostic" in domain_lower:
            return "diagnostics"
        return domain_lower
